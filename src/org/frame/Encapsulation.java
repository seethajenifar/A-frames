package org.frame;
public class Encapsulation {
private int id;
private String name;
private long phone;
public void setid(int id) {
this.id=id;
}
public void setName(String name) {
this.name=name;
}
public void setphone(long phone) {
this.phone=phone;
}
public int getid() {
return id;
}
public String getname() {
	return name;
}
public long getphone() {	
	return phone;
}}
